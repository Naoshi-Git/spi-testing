export const parseVocabularyData = (markdown: string): WordEntry[] => {
  const lines = markdown.split('\n');
  const words: WordEntry[] = [];
  let idCounter = 0;

  for (const line of lines) {
    const trimmedLine = line.trim();
    if (trimmedLine.startsWith('| ') && !trimmedLine.startsWith('|:') && !trimmedLine.includes('**【')) {
      const parts = trimmedLine.split('|').map(s => s.trim());
      // parts will be like ['', '単語', 'よみ', '意味', '備考', '']
      if (parts.length >= 5 && parts[1] && parts[3]) { // Need at least word and meaning
        words.push({
          id: `word-${idCounter++}`,
          word: parts[1],
          reading: parts[2] || '', // よみ can be empty
          meaning: parts[3],
          remarks: parts[4] || '', // 備考 can be empty
        });
      }
    }
  }
  return words;
};
